function functionConditions(x) {
    if (x == 'Wine') {
        console.log(x + ' What is it, Tuesday?');
    } else if (x == 'Tequila') {
        console.log(x + ' Sounds like a good choice.');
    } else if (x == 'Vodka') {
        console.log(x + ' Grow up.');
    } else if (x == 'Gin') {
        console.log(x + ' I guess.');
    } else if (x == 'Rum') {
        console.log(x + ' Now you have taste!');
    } else if (x == 'Whiskey') {
        console.log(x + ' Say less.');
    } else {
        console.log('Sorry, but absolutely not');
    }
}
